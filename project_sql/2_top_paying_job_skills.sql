/*
    Question: What are the skills required for these top-paying roles?
    - Use the top 10 highest-paying Data Analyst jobs from first query.
    - Add the specific skills required for these roles
    - Why? It provides a detailed look at which high-paying job demand certain skills, helping job seekers understands which skills to develop that aligns with top salaries
*/

WITH top_paying_roles AS (
    SELECT 
        j.job_id,
        c.name  AS company_name,
        j.job_title,
        j.salary_year_avg
    FROM 
        job_postings_fact AS j
    LEFT JOIN company_dim AS c 
        ON j.company_id = c.company_id
    WHERE 
        j.job_work_from_home = true 
        AND j.salary_year_avg IS NOT NULL 
        AND j.job_title_short = 'Data Analyst'
    ORDER BY 
        j.salary_year_avg DESC
    LIMIT 10

)

SELECT 
    top_paying_roles.*,
    skills_table.skills,
    skills_table.type
FROM top_paying_roles
INNER JOIN (
    SELECT 
        sjd.job_id,
        sd.*
    FROM skills_job_dim AS sjd
    INNER JOIN skills_dim AS sd 
        ON sd.skill_id = sjd.skill_id
) AS skills_table
    ON top_paying_roles.job_id = skills_table.job_id
ORDER BY salary_year_avg



